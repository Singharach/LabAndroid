package com.example.lab11androidgrvglide.model;

public class Gun {
    String gun_name;
    String gun_image;
    int gun_price;

    public String getGun_name() {
        return gun_name;
    }

    public void setGun_name(String gun_name) {this.gun_name = gun_name; }

    public String getGun_image() {
        return gun_image;
    }

    public void setGun_image(String gun_image) {
        this.gun_image = gun_image;
    }

    public int getGun_price() {
        return gun_price;
    }

    public void setGun_price(int gun_price) {
        this.gun_price = gun_price;
    }
}
